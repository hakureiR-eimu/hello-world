#include<stdio.h>           /*输入输出头文件*/

int main()/*空类型：主函数*/

{
int n;
int b[16],x,k,r,i;/*定义变量和数组的数据类型为整型*/

printf("输入一个十进制:");/*输出文字提示*/

scanf("%d",&x);         /*输入一个十进制数字*/

k=-1;/*将-1赋给k*/

do/*用循环将十进制的数转化成二进制的数*/

{

r=x%2;/*用除R取余倒计法计算二进制*/

b[++k]=r;/*将值赋给数组*/

x/=2;/*将原数除2*/

    }while(x>=1);

for(i=k,n=0;i>=0;i--,n++)/*将上面倒计的数正回来*/
b[n]=b[i];
printf("%d",b);/*输出结果*/

printf("\n");
return 0;
}

